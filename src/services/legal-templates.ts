export function renderDocument(type: 'NDA' | 'NCNDA' | 'COMMISSION', p: Context): string {
  switch (type) {
    case 'NDA': return renderNDA(p);
    case 'NCNDA': return renderNCNDA(p);
    case 'COMMISSION': return renderCommission(p);
  }
}
